# Aula 26 Operadores aritméticos
# inteiro e inteiro geralmente retorna um numero inteiro, mas pode retornar um float
# inteiro e float geralmente retorna um float
adicao = 10 + 10

print('Adicao', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 100 / 10 # SEMPRE retorna float
print('divisão', divisao)

divisao_inteira = 100 // 19 # Retorna sem numeros que sobraram. Geralmente retorna inteiro
print('Divisao inteira', divisao_inteira)

exponenciacao = 10 ** 3
print('Exponenciacao', exponenciacao)

modulo = 100 % 2 # Resto da divisão
print('Módulo', (modulo))
